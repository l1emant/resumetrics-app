'use client';

import { useState } from 'react';
import { AnalysisResult } from '@/lib/llm';
import { ArrowLeft, CheckCircle2, AlertTriangle, Tag, ArrowRightLeft, Copy, Check, Download } from 'lucide-react';

/* ─── Score Ring ─── */
export function ScoreRing({ score, size = 80 }: { score: number; size?: number }) {
  const radius = (size / 2) - 6;
  const circumference = 2 * Math.PI * radius;
  const offset = circumference - (score / 100) * circumference;
  const color = score >= 70 ? '#10b981' : score >= 40 ? '#eab308' : '#ef4444';

  return (
    <div className="relative flex items-center justify-center" style={{ width: size, height: size }}>
      <svg className="-rotate-90" width={size} height={size} viewBox={`0 0 ${size} ${size}`}>
        <circle cx={size/2} cy={size/2} r={radius} fill="none" stroke="var(--score-track)" strokeWidth="5" />
        <circle
          cx={size/2} cy={size/2} r={radius} fill="none"
          stroke={color} strokeWidth="5" strokeLinecap="round"
          strokeDasharray={circumference} strokeDashoffset={offset}
          className="transition-all duration-1000 ease-out"
        />
      </svg>
      <div className="absolute inset-0 flex flex-col items-center justify-center">
        <span className="text-xl font-bold text-foreground">{score}</span>
      </div>
    </div>
  );
}

/* ─── Mini Bar ─── */
export function MiniBar({ label, value, max = 25 }: { label: string; value: number; max?: number }) {
  const pct = Math.min((value / max) * 100, 100);
  const color = pct >= 70 ? 'bg-emerald-500' : pct >= 40 ? 'bg-yellow-500' : 'bg-red-500';
  return (
    <div className="flex items-center gap-3">
      <span className="text-[11px] text-muted-foreground w-24 shrink-0">{label}</span>
      <div className="flex-1 h-1.5 bg-muted rounded-full overflow-hidden">
        <div className={`h-full rounded-full ${color} transition-all duration-700`} style={{ width: `${pct}%` }} />
      </div>
      <span className="text-[11px] text-muted-foreground w-8 text-right">{value}/{max}</span>
    </div>
  );
}

/* ─── Section ─── */
function Section({ title, icon, children }: { title: string; icon: React.ReactNode; children: React.ReactNode }) {
  return (
    <div className="bg-[var(--surface-card)] border border-[var(--surface-card-border)] rounded-xl overflow-hidden">
      <div className="flex items-center gap-2 px-4 py-2.5 border-b border-[var(--surface-card-border)]">
        {icon}
        <span className="text-xs font-medium text-foreground uppercase tracking-wider">{title}</span>
      </div>
      <div className="px-4 py-3">
        {children}
      </div>
    </div>
  );
}

/* ─── Format results as plain text ─── */
function formatResultsAsText(r: AnalysisResult): string {
  const lines: string[] = [];
  lines.push('═══ RESUME ANALYSIS REPORT ═══');
  lines.push('');
  lines.push(`ATS Score: ${r.score}/100`);
  lines.push(`  Keywords: ${r.scoreBreakdown.keywordMatch}/25`);
  lines.push(`  Formatting: ${r.scoreBreakdown.formatting}/25`);
  lines.push(`  Experience: ${r.scoreBreakdown.experience}/25`);
  lines.push(`  Skills: ${r.scoreBreakdown.skills}/25`);
  lines.push('');
  lines.push(`Summary: ${r.summary}`);

  if (r.strengths.length > 0) {
    lines.push('');
    lines.push('── Strengths ──');
    r.strengths.forEach(s => lines.push(`  • ${s}`));
  }
  if (r.improvements.length > 0) {
    lines.push('');
    lines.push('── Improvements ──');
    r.improvements.forEach(s => lines.push(`  • ${s}`));
  }
  if (r.missingKeywords.length > 0) {
    lines.push('');
    lines.push('── Missing Keywords ──');
    lines.push(`  ${r.missingKeywords.join(', ')}`);
  }
  if (r.rewriteSuggestions.length > 0) {
    lines.push('');
    lines.push('── Rewrite Suggestions (XYZ Format) ──');
    r.rewriteSuggestions.forEach((s, i) => {
      lines.push(`  ${i + 1}. Before: ${s.original || '(no original)'}`);
      lines.push(`     After:  ${s.improved}`);
    });
  }

  lines.push('');
  lines.push('Generated by Resumetrics');
  return lines.join('\n');
}

/* ─── Dashboard ─── */
export function ResultsDashboard({ result, onReset }: { result: AnalysisResult | null; onReset: () => void }) {
  const [copied, setCopied] = useState(false);

  if (!result) return null;

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(formatResultsAsText(result));
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // Fallback for older browsers
      const textarea = document.createElement('textarea');
      textarea.value = formatResultsAsText(result);
      document.body.appendChild(textarea);
      textarea.select();
      document.execCommand('copy');
      document.body.removeChild(textarea);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    }
  };

  const handleExportPDF = () => {
    window.print();
  };

  return (
    <div id="results-dashboard" className="flex flex-col gap-3 animate-in fade-in slide-in-from-bottom-4 duration-500 pb-12">
      
      {/* Header: Back + Actions */}
      <div className="flex items-center justify-between mb-1">
        <button onClick={onReset} className="print-hidden flex items-center gap-1.5 text-[11px] text-muted-foreground hover:text-foreground transition-colors w-fit cursor-pointer">
          <ArrowLeft className="w-3 h-3" /> New Analysis
        </button>
        <div className="print-hidden flex items-center gap-1">
          <button
            onClick={handleCopy}
            title={copied ? 'Copied!' : 'Copy results'}
            className="flex items-center gap-1.5 px-2.5 py-1.5 rounded-lg text-[11px] text-muted-foreground hover:text-foreground hover:bg-muted transition-all cursor-pointer"
          >
            {copied ? (
              <><Check className="w-3 h-3 text-emerald-500" /><span className="text-emerald-500">Copied</span></>
            ) : (
              <><Copy className="w-3 h-3" /><span>Copy</span></>
            )}
          </button>
          <button
            onClick={handleExportPDF}
            title="Export as PDF"
            className="flex items-center gap-1.5 px-2.5 py-1.5 rounded-lg text-[11px] text-muted-foreground hover:text-foreground hover:bg-muted transition-all cursor-pointer"
          >
            <Download className="w-3 h-3" />
            <span>PDF</span>
          </button>
        </div>
      </div>

      {/* Score Overview */}
      <div className="flex items-start gap-5 bg-[var(--surface-card)] border border-[var(--surface-card-border)] rounded-xl p-4">
        <ScoreRing score={result.score} />
        <div className="flex-1 pt-1">
          <p className="text-xs text-muted-foreground uppercase tracking-wider mb-1">ATS Score</p>
          <p className="text-sm text-foreground leading-relaxed">{result.summary}</p>
          <div className="mt-3 flex flex-col gap-1.5">
            <MiniBar label="Keywords" value={result.scoreBreakdown.keywordMatch} />
            <MiniBar label="Formatting" value={result.scoreBreakdown.formatting} />
            <MiniBar label="Experience" value={result.scoreBreakdown.experience} />
            <MiniBar label="Skills" value={result.scoreBreakdown.skills} />
          </div>
        </div>
      </div>

      {/* Strengths */}
      {result.strengths.length > 0 && (
        <Section title="Strengths" icon={<CheckCircle2 className="w-3.5 h-3.5 text-emerald-500" />}>
          <ul className="flex flex-col gap-2">
            {result.strengths.map((s, i) => (
              <li key={i} className="flex items-start gap-2 text-[13px] text-foreground/80 leading-relaxed">
                <span className="text-emerald-500 mt-0.5 shrink-0">•</span>
                {s}
              </li>
            ))}
          </ul>
        </Section>
      )}

      {/* Improvements */}
      {result.improvements.length > 0 && (
        <Section title="Improvements" icon={<AlertTriangle className="w-3.5 h-3.5 text-yellow-500" />}>
          <ul className="flex flex-col gap-2">
            {result.improvements.map((s, i) => (
              <li key={i} className="flex items-start gap-2 text-[13px] text-foreground/80 leading-relaxed">
                <span className="text-yellow-500 mt-0.5 shrink-0">•</span>
                {s}
              </li>
            ))}
          </ul>
        </Section>
      )}

      {/* Missing Keywords */}
      {result.missingKeywords.length > 0 && (
        <Section title="Missing Keywords" icon={<Tag className="w-3.5 h-3.5 text-red-400" />}>
          <div className="flex flex-wrap gap-1.5">
            {result.missingKeywords.map((kw, i) => (
              <span key={i} className="px-2 py-0.5 bg-red-500/10 border border-red-500/20 rounded-full text-[11px] text-red-500 dark:text-red-400">
                {kw}
              </span>
            ))}
          </div>
        </Section>
      )}

      {/* Rewrite Suggestions */}
      {result.rewriteSuggestions.length > 0 && (
        <Section title="Rewrites" icon={<ArrowRightLeft className="w-3.5 h-3.5 text-blue-500 dark:text-blue-400" />}>
          <div className="flex flex-col gap-3">
            {result.rewriteSuggestions.map((s, i) => (
              <div key={i} className="flex flex-col gap-1.5">
                {s.original && (
                  <div className="flex items-start gap-2">
                    <span className="text-[10px] uppercase text-muted-foreground w-12 shrink-0 pt-0.5">Before</span>
                    <p className="text-xs text-muted-foreground leading-relaxed line-through decoration-muted-foreground/30">{s.original}</p>
                  </div>
                )}
                <div className="flex items-start gap-2">
                  <span className="text-[10px] uppercase text-emerald-600 dark:text-emerald-500 w-12 shrink-0 pt-0.5">After</span>
                  <p className="text-xs text-emerald-700 dark:text-emerald-300/80 leading-relaxed">{s.improved}</p>
                </div>
                {i < result.rewriteSuggestions.length - 1 && <hr className="border-[var(--surface-card-border)] mt-1" />}
              </div>
            ))}
          </div>
        </Section>
      )}
    </div>
  );
}
